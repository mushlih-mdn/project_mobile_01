void main() {
  var nilai = 'A';
  switch (nilai) {
    case 'A':
      print('Nilai Anda Sangat Baik');
      break;
    case 'B':
      print('Nilai Anda Baik');
      break;
    case 'C':
      print('Nilai Anda Cukup');
      break;
    case 'D':
      print('Nilai Anda Kurang');
      break;
    case 'E':
      print('Nilai Anda Sangat Kurang');
      break;
    default:
      print('Nilai Tidak Valid');
  }
}
